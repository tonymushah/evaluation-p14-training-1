CREATE VIEW V_ENTREE_STOCK_POINT_VENTE AS
SELECT EPV.ID_ENTREE_STOCK,
	EPV.ENTREE_DATE,
	V_DT.ID_DEMANDE,
	V_DT.DATE_DEMANDE,
	V_DT.POINT_VENTE,
	V_DT.ENTREE_MAGASIN,
	V_DT.DATE_ENTREE_MAGASIN,
	V_DT.LAPTOP
FROM ENTREE_STOCK_POINT_VENTE AS EPV
JOIN V_DEMANDE_TRANSFERT AS V_DT ON EPV.SORTIE_STOCK = V_DT.ID_DEMANDE;